-- Port-mapped-IO variant of the Z80 walking counter: a vendored T80 Z80 core
-- (below, verbatim, BSD-3, with a numeric_std_unsigned standardization patch) +
-- ROM + RAM + an IO register file in the Z80 I/O space + a top whose adapter
-- exposes MREQ/IORQ so the decode can split memory and I/O accesses.
--
-- The firmware (firmware/t80_portio_counter_7seg.asm) polls the prescaler tick and
-- drives the display exactly like the memory-mapped design, but reads switches /
-- buttons / config and writes LEDs / segments with IN/OUT (I/O space) rather than
-- loads/stores.  Visible behavior is identical: a one-hot LED bounces across the
-- LEDs while a decimal odometer advances; btn(0) reverses both directions, btn(1)
-- is a lamp test, and each active switch steps faster.  The Z80 boots at $0000, so
-- ROM is at the bottom of the memory map.  See docs/embedded_core_system_plan.md.
--
