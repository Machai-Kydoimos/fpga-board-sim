--
-- Interrupt-driven (Z80 IM 2) variant of the Z80 walking counter: a vendored T80
-- Z80 core (below, verbatim, BSD-3, with a numeric_std_unsigned standardization
-- patch) + ROM + RAM + memory-mapped IO + a top whose interrupt controller drives
-- the CPU's INT line and puts a per-source vector on the bus during INTA.
--
-- The firmware (firmware/t80_irq_counter_7seg.asm) arms interrupt mode 2 and idles;
-- the hardware routes the timer source (vector $00) and the sw/btn-change source
-- (vector $02) to separate ISRs via the I:vector table at $0100.  Visible behavior
-- matches the polled design: a one-hot LED bounces across the LEDs while a decimal
-- odometer advances; btn(0) reverses both directions, btn(1) is a lamp test, and
-- each active switch steps faster.  The Z80 boots at $0000, so ROM is at the bottom
-- of the memory map.  See docs/embedded_core_system_plan.md.
--
