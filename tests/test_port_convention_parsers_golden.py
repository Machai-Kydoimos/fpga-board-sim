def _stored_convention() -> dict[str, Any]:
    with open(BOARDS_DIR / "digilent-xdc" / "basys_3.json") as f:
        board = json.load(f)
    convention: dict[str, Any] = board["port_conventions"]["digilent"]
    return convention


def test_reproduces_stored_clk_leds_switches_buttons() -> None:
    table = parse(_BASYS3_MASTER_XDC)
    result = classify(table)
    stored = _stored_convention()

    assert result["clk"] == stored["clk"]
    assert result["leds"] == stored["leds"]
    assert result["switches"] == stored["switches"]
    # buttons: the two pipelines now diverge deliberately.  This classifier
    # still synthesizes a shared `btn` vector from the btnC/U/L/R/D scalars
    # (and, per classify.py's module docstring, leaves polarity unset -- no
    # dialect states it as syntax).  The stored block is the U38 corrected
    # form: the five directionals as a `names[]` scalar bank (the real XDC
    # ports -- a `btn[4:0]` vector never existed on this board), with the
    # active_low the digilent parser cites from the RM.  As with seven_seg
    # below, the on-disk JSON leads and the classifier may catch up later.
    assert result["buttons"] == {"name": "btn", "width": 5}
    assert stored["buttons"] == {
        "names": ["btnC", "btnU", "btnD", "btnL", "btnR"],
        "active_low": False,
    }


def test_clock_frequency_matches_boards_default_clock_hz() -> None:
    table = parse(_BASYS3_MASTER_XDC)
    assert table.clocks[0].frequency_hz == 100e6


def test_seven_seg_is_correctly_reclassified_as_scan() -> None:
    # See the module docstring: since U22 Phase D the stored JSON agrees with
    # the corrected reading; classify's structural fields must match it, and
    # the stored block carries the dp + cited-polarity enrichment on top.
    table = parse(_BASYS3_MASTER_XDC)
    result = classify(table)
    stored = _stored_convention()

    assert stored["seven_seg"]["style"] == "scan"
    assert result["seven_seg"] == {
        "style": "scan",
        "name": stored["seven_seg"]["name"],
        "width_per_digit": stored["seven_seg"]["width_per_digit"],
        "digit_enable": {
            "name": stored["seven_seg"]["digit_enable"]["name"],
            "width": stored["seven_seg"]["digit_enable"]["width"],
        },
    }
    # The enrichment classify deliberately does not produce:
    assert stored["seven_seg"]["dp"] == "dp"
    assert stored["seven_seg"]["active_low"] is True
    assert stored["seven_seg"]["digit_enable"]["active_low"] is True


def test_decimal_point_and_pmod_headers_are_excluded() -> None:
    # "dp" is a lone decimal-point pin (no seg/hex substring -> not seg-interest);
    # JA/JB/JC/JXADC/vga*/Rs*/PS2*/Qspi* must not leak into any bucket. A leak
    # would show up as e.g. an inflated led/switch width, which the exact-match
    # assertions above would already catch -- this makes that guarantee explicit.
    table = parse(_BASYS3_MASTER_XDC)
    serialized = json.dumps(classify(table))
    for leaked in ("JA", "JB", "JC", "JXADC", "vga", "Rs", "PS2", "Qspi", '"dp"'):
        assert leaked not in serialized
